// advantages of external javascript
// 1. DRY (Don't Repeat Yourself): You can write the code once and reuse it across multiple HTML files.
// 2. Browser Caching: When you link to an external JavaScript file, the browser caches it. This means that if the same file is used on multiple pages, it only needs to be downloaded once.
// 3. Separation of Concerns: Keeping JavaScript in separate files helps to keep your HTML clean and organized.
// 4. Easier Maintenance: If you need to make changes to your JavaScript code, you only have to do it in one place.
// 5. Improved Load Times: By using external JavaScript files, you can improve the load times of your web pages.
// 6. Collaboration: When working in teams, having separate files for JavaScript can make it easier for multiple developers to work on the same project without conflicts.
